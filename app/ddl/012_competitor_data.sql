-- Creates views over Cybersyn SEC data for competitor intelligence
-- and MSHA quarry site data table
-- ============================================================================

USE DATABASE VULCAN_MATERIALS_DB;
USE SCHEMA ATOMIC;

-- ============================================================================
-- COMPETITOR FINANCIALS VIEW (Cybersyn SEC XBRL data)
-- Already exists in FEATURE_STORE as COMPETITOR_FEATURES$1; this adds
-- the full-detail ATOMIC view for ad-hoc queries and the agent.
-- ============================================================================

CREATE OR REPLACE VIEW VULCAN_MATERIALS_DB.ATOMIC.COMPETITOR_FINANCIALS AS
SELECT mt.COMPANY_NAME, mt.CIK, mt.VARIABLE_NAME, mt.TAG, mt.VALUE, mt.UNIT,
       mt.BUSINESS_SEGMENT, mt.GEO_NAME, mt.FISCAL_PERIOD, mt.FISCAL_YEAR,
       mt.PERIOD_START_DATE, mt.PERIOD_END_DATE
FROM SNOWFLAKE_PUBLIC_DATA_FREE.PUBLIC_DATA_FREE.SEC_METRICS_TIMESERIES mt
WHERE mt.CIK IN ('0001396009','0000916076','0000849395','0000918646','0001571371')
  AND LOWER(mt.VARIABLE_NAME) LIKE '%revenue%';

-- ============================================================================
-- SIC PEER UNIVERSE (all SEC filers in SIC 14xx — mining/quarrying)
-- ============================================================================

CREATE OR REPLACE VIEW VULCAN_MATERIALS_DB.ATOMIC.SIC_PEERS AS
SELECT CIK, COMPANY_NAME, SIC, SIC_CODE_DESCRIPTION, STATE, CITY
FROM SNOWFLAKE_PUBLIC_DATA_FREE.PUBLIC_DATA_FREE.SEC_CIK_INDEX
WHERE SIC LIKE '14%';

-- ============================================================================
-- MSHA QUARRY SITES TABLE (loaded from MSHA Open Government Data)
-- Source: https://arlweb.msha.gov/OpenGovernmentData/DataSets/Mines.zip
-- Filtered to: Active/Intermittent, Stone (canvass 6) and Sand & Gravel (5)
-- ============================================================================

CREATE TABLE IF NOT EXISTS VULCAN_MATERIALS_DB.ATOMIC.MSHA_QUARRY_SITES (
    MINE_ID VARCHAR(10),
    MINE_NAME VARCHAR(200),
    MINE_TYPE VARCHAR(50),
    MINE_STATUS VARCHAR(50),
    OPERATOR_NAME VARCHAR(200),
    OPERATOR_GROUP VARCHAR(50),
    CONTROLLER_NAME VARCHAR(200),
    STATE VARCHAR(5),
    COUNTY VARCHAR(100),
    LATITUDE NUMBER(10,7),
    LONGITUDE NUMBER(11,7),
    REGION_CODE VARCHAR(20),
    PRIMARY_SIC VARCHAR(100),
    PRIMARY_CANVASS VARCHAR(20),
    NO_EMPLOYEES NUMBER(10,0),
    NEAREST_TOWN VARCHAR(100),
    DATA_SOURCE VARCHAR(20) DEFAULT 'MSHA'
);

