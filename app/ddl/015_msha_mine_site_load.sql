-- ============================================================================
-- WORKSTREAM C: MSHA QUARRY DATA LOAD
-- ============================================================================
-- Loads MSHA public mine/quarry data into SNOWCORE_INDUSTRIES.MINING.MINE_SITE
-- Source: https://arlweb.msha.gov/OpenGovernmentData/DataSets/Mines.zip
-- Filtered: Active/Intermittent, Stone (canvass 6) + Sand & Gravel (canvass 5)
-- Total: ~10,343 quarry sites with GPS coordinates
-- ============================================================================

-- Step 1: Create staging table (data loaded via Python ETL)
-- See: data/msha/quarries_clean.csv (pipe-delimited, generated by ETL script)

CREATE STAGE IF NOT EXISTS VULCAN_MATERIALS_DB.ATOMIC.MSHA_STAGE
  FILE_FORMAT = (TYPE = 'CSV' FIELD_DELIMITER = '|' SKIP_HEADER = 1
                 FIELD_OPTIONALLY_ENCLOSED_BY = '"' ERROR_ON_COLUMN_COUNT_MISMATCH = FALSE);

-- PUT file://data/msha/quarries_clean.csv @VULCAN_MATERIALS_DB.ATOMIC.MSHA_STAGE;

COPY INTO VULCAN_MATERIALS_DB.ATOMIC.MSHA_QUARRY_SITES (
    MINE_ID, MINE_NAME, MINE_TYPE, MINE_STATUS, OPERATOR_NAME, OPERATOR_GROUP,
    CONTROLLER_NAME, STATE, COUNTY, LATITUDE, LONGITUDE, REGION_CODE,
    PRIMARY_SIC, PRIMARY_CANVASS, NO_EMPLOYEES, NEAREST_TOWN
)
FROM @VULCAN_MATERIALS_DB.ATOMIC.MSHA_STAGE/quarries_clean.csv
FILE_FORMAT = (TYPE = 'CSV' FIELD_DELIMITER = '|' SKIP_HEADER = 1
               FIELD_OPTIONALLY_ENCLOSED_BY = '"' ERROR_ON_COLUMN_COUNT_MISMATCH = FALSE);

-- Step 2: Populate SNOWCORE_INDUSTRIES.MINING.MINE_SITE

INSERT INTO SNOWCORE_INDUSTRIES.MINING.MINE_SITE (
    MINE_SITE_ID, SITE_CODE, SITE_NAME, MINE_TYPE, PRIMARY_COMMODITY,
    SITE_STATUS, GPS_LATITUDE, GPS_LONGITUDE, CREATED_BY_USER
)
SELECT
    ROW_NUMBER() OVER (ORDER BY MINE_ID) AS MINE_SITE_ID,
    'MSHA-' || MINE_ID AS SITE_CODE,
    MINE_NAME AS SITE_NAME,
    CASE
        WHEN MINE_TYPE = 'Surface' THEN 'QUARRY'
        WHEN MINE_TYPE = 'Underground' THEN 'UNDERGROUND'
        ELSE 'QUARRY'
    END AS MINE_TYPE,
    CASE
        WHEN PRIMARY_CANVASS = 'Stone' THEN 'STONE'
        WHEN PRIMARY_CANVASS = 'SandAndGravel' THEN 'SAND_GRAVEL'
        ELSE 'OTHER'
    END AS PRIMARY_COMMODITY,
    CASE
        WHEN MINE_STATUS = 'Active' THEN 'OPERATING'
        WHEN MINE_STATUS = 'Intermittent' THEN 'CARE_MAINTENANCE'
        ELSE 'OPERATING'
    END AS SITE_STATUS,
    LATITUDE,
    LONGITUDE,
    'MSHA_DATA_LOAD' AS CREATED_BY_USER
FROM VULCAN_MATERIALS_DB.ATOMIC.MSHA_QUARRY_SITES;

-- Step 3: Verify

SELECT MINE_TYPE, PRIMARY_COMMODITY, SITE_STATUS, COUNT(*) as CNT
FROM SNOWCORE_INDUSTRIES.MINING.MINE_SITE
GROUP BY MINE_TYPE, PRIMARY_COMMODITY, SITE_STATUS
ORDER BY CNT DESC;

SELECT OPERATOR_GROUP, COUNT(*) as SITES, SUM(NO_EMPLOYEES) as EMPLOYEES
FROM VULCAN_MATERIALS_DB.ATOMIC.MSHA_QUARRY_SITES
WHERE OPERATOR_GROUP != 'OTHER'
GROUP BY OPERATOR_GROUP
ORDER BY SITES DESC;
